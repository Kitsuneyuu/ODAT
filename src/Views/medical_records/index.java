    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked

        int column = tabla.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tabla.getRowHeight();

        if (row < tabla.getRowCount() && row >= 0 && column < tabla.getColumnCount() && column >= 0) {
            Object value = tabla.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                if (boton.getName().equals("+")) {
                    if (subtable_open) {
                        subtable_hide();
                        row -= 10;
                    }
                    subtable_index = row;
                    Button btn = new Button();
                    DefaultTableModel model = (DefaultTableModel) tabla.getModel();
                    model.setValueAt(btn.getButton("-"), row, 0);
                    Object rowData[] = {"", "", "", "", "", "", "", ""};
                    for (int x = 0; x <= 9; x++) {
                        model.insertRow(row + 1, rowData);
                    }
                    subtable_open = true;
                    sub = new subtable(controller.getModel(row).getId());
                    sub.setBounds(0, (row + 1) * 30, 750, 300);
                    tabla.add(sub);
                    
                    
                }

                if (boton.getName().equals("m")) {
                    Models.medical_record model = controller.getModel(row);
                    form frame = new form(model, this, row);
                    frame.setLocationRelativeTo(null);
                    frame.setVisible(true);
                    this.setEnabled(false);
                }

                if (boton.getName().equals("d")) {
                    medical_record m = controller.getModel(row);
                    Object[] options = {"Aceptar", "Cancelar"};
                    int n = JOptionPane.showOptionDialog(this, "Desea eliminar el registro: " + m.getName() + "?",
                            "Eliminar registro",
                            JOptionPane.YES_NO_OPTION,
                            JOptionPane.ERROR_MESSAGE,
                            null,
                            options,
                            options[0]);
                    if (n == 0) {
                        controller.delete(m.getId());
                        controller.list.remove(row);

                        ((DefaultTableModel) tabla.getModel()).removeRow(row);
                    }
                }

                if (boton.getName().equals("-")) {
                    subtable_open = false;
                    subtable_hide();
                    Button btn = new Button();
                    DefaultTableModel model = (DefaultTableModel) tabla.getModel();
                    model.setValueAt(btn.getButton("+"), row, 0);
                }
            }
        }
    }//GEN-LAST:event_tablaMouseClicked

    private void subtable_hide() {
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        Button btn = new Button();
        model.setValueAt(btn.getButton("+"), subtable_index, 0);
        for (int x = 0; x <= 9; x++) {
            model.removeRow(subtable_index + 1);
        }
        tabla.remove(sub);
    }

